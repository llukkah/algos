///////////////Create a function which takes two arguments,//////
/////////////// an array (arr) and an integer (num)./////////////
//////////////Determine the index number of the array///////////
///////////// at which the num should be inserted into/////////
///////////////////// the sorted arr./////////////////////////////
///////////////The output should return integer //////////////////
////////////////representing the index number.////////////////////
//////////////////////////////////////////////////////////////////
const getIndexToInsert = (arr, num) => {
    arr.push(num);
    let sortArr = arr.sort((a, b) => a - b);
    return sortArr.indexOf(num);
  };
